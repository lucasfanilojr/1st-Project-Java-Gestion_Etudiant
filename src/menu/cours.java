    private void jButton4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton4ActionPerformed
        try {
            String codeC = codecours.getSelectedItem().toString();
            String nom = champnomcours.getText();
            String coef = champcoeff.getText();
            pst = con.prepareStatement("update cours set nom=? , coefficient=? where codeC=? ");
            pst.setString(1, nom);            
            pst.setString(2, coef);
            pst.setString(3, codeC);
            int k = pst.executeUpdate();
            if (k==1) {
                JOptionPane.showMessageDialog(this, "le cours est modifier avec success");
                champnomcours.setText("");
                champcoeff.setText("");
                codecours();
                listeC();
            }else{
                JOptionPane.showMessageDialog(this, "une erreur est survenue pendant la modification");
            }
        } catch (SQLException ex) {
            Logger.getLogger(cours.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_jButton4ActionPerformed

