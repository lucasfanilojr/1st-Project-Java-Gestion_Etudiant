    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        try {
            String nomCours = champnomcours.getText();
            String coeff = champcoeff.getText();
            pst = con.prepareStatement("insert into cours (nom,coefficient) values(?,?)  ");            
            pst.setString(1, nomCours);
            pst.setString(2, coeff);
            int k = pst.executeUpdate();
            if (k==1) {
                JOptionPane.showMessageDialog(this, "le cours est ajouter avec success");
                champnomcours.setText("");
                champcoeff.setText("");
                codecours();
                listeC();
            }else{
                JOptionPane.showMessageDialog(this, "une erreur est survenue pendant l'insertion");
            }
        } catch (SQLException ex) {
            Logger.getLogger(cours.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_jButton3ActionPerformed

